package com.mycompany.vaderanalys;

/**
 *
 * @author Vikke
 */
public class YearTemp {
    private int year;
    private float temp;
    public YearTemp(int year, float temp)
    {
        this.year = year;
        this.temp = temp;
    }
    public int getYearY()
    {
       return year;
    }
    public float getTempY()
    {
        return temp;
    }
}
